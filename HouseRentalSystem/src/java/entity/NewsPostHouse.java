package entity;

import java.sql.Date;

/**
 *
 * @author ADMIN
 */
public class NewsPostHouse {

    private int id;
    private String title, price, image;

    public NewsPostHouse() {
    }

    public NewsPostHouse(int id, String title, String price, String image) {
        this.id = id;
        this.title = title;
        this.price = price;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
}
